// ** неявное преобразование, шаблонная строка `${}`
`Число ${40 + 2} - ответ на вопрос о жизни, вселенной и вообще!`; // 'Число 42 - ответ на вопрос о жизни, вселенной и вообще!'

`Если в вашей программе пользователи увидели ${{}}, ${true}, ${false} или ${undefined}, то вы совершенно точно, что-то сделали не так!`; // 'Если в вашей программе пользователи увидели [object Object], true, false или undefined, то вы совершенно точно, что-то сделали не так!'
